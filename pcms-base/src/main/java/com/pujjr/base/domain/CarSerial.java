package com.pujjr.base.domain;

public class CarSerial {
    private String id;

	private String carBrandId;

	private String carSerialGroup;

	private String carSerialName;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getCarBrandId() {
		return carBrandId;
	}

	public void setCarBrandId(String carBrandId) {
		this.carBrandId = carBrandId;
	}

	public String getCarSerialGroup() {
		return carSerialGroup;
	}

	public void setCarSerialGroup(String carSerialGroup) {
		this.carSerialGroup = carSerialGroup;
	}

	public String getCarSerialName() {
		return carSerialName;
	}

	public void setCarSerialName(String carSerialName) {
		this.carSerialName = carSerialName;
	}
}